    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try{
          
            PreparedStatement pst =con.prepareCall("SELECT l.lessonID, l.lessonName, COUNT(s.studentID) AS numberOfStudent FROM student s, lessonenrollment le, lesson l WHERE l.lessonID = le.FK3_lessonID AND le.FK2_studentID = s.studentID");
            ResultSet rs = pst.executeQuery();
            DefaultTableModel tm = (DefaultTableModel)jTable1.getModel();
            tm.setRowCount(0);
            
            while(rs.next()){
                Object o[] = {rs.getInt("lessonID"),rs.getString("lessonName"),rs.getString("numberOfStudent")};
                tm.addRow(o);
                
            }
            //JOptionPane.showMessageDialog(rootPane, "");
        } catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

